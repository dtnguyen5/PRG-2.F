public class Switch {
    public static void main(String[] args) {

        int a = 8;

        switch (a){
            case 5:
                System.out.println("je sobota");
                break;
            case 6:
                System.out.println("je neděle");
                break;
            default:
                System.out.println("Je pracovní týden");
                break;



        }
        // ctrl + alt + l



    }
}

